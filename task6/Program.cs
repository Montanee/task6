bool ПроверитьПалиндром(string строка)
{
    строка = строка.ToLower().Replace(" ", ""); // Преобразуем строку в нижний регистр и удаляем пробелы

    int начало = 0;
    int конец = строка.Length - 1;

    while (начало < конец)
    {
        if (строка[начало] != строка[конец])
        {
            return false; // Если символы не совпадают, строка не является палиндромом
        }
        начало++;
        конец--;
    }

    return true; // Если все символы совпадают, строка является палиндромом
}
